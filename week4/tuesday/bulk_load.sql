-- Load CSV data
COPY INTO RAW_PRODUCTS (product_id, product_name, category, price, quantity_sold, sale_date)
FROM @CSV_LOAD_STAGE
FILE_FORMAT = (FORMAT_NAME = MY_CSV_FORMAT)
ON_ERROR = 'CONTINUE'
PURGE = FALSE;

